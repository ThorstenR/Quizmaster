	/**
	 * Displays all the output text
	 */
	public void displayOutput() {
		lblCongrats.setText(
			String.format(
				"Hallo %s, Sie haben %d von %d (%d %%) Fragen richtig beantwortet. Herzlichen Glückwunsch!",
				currentUser.getUsername(),
				correctQuestions.size(),
				correctQuestions.size() + wrongQuestions.size(),
				(100 * correctQuestions.size()) / correctQuestions.size() + wrongQuestions.size()
			)
		);
		
		showWrongQuestions();
	}
	
	/**
	 * Lists the wrong questions and related choices (wrong choice / correct choice)
	 */
	private void showWrongQuestions() {
		JPanel panelButton = new JPanel(new GridLayout(2,3));
		
		for(WrongQuestion wq: wrongQuestions) {
			JLabel q = new JLabel(wq.getQuestion());
			JLabel wa = new JLabel(wq.getWrongAnswer());
			JLabel ca = new JLabel(wq.getCorrectAnswer());
			
			panelButton.add(q);
			panelButton.add(wa);
			panelButton.add(ca);
		}
		
		this.add(panelButton);
	}

